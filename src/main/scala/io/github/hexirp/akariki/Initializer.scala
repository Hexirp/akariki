package io.github.hexirp.akariki

import net.minecraftforge.fml.common.ModMetadata
import net.minecraftforge.fml.common.event.{ //
  FMLInitializationEvent, FMLPostInitializationEvent, FMLPreInitializationEvent}

import org.apache.logging.log4j.Logger

/** [[Initializer]] receives Forge's initialization events.
  *
  * @param context The context of initialization
  */
class Initializer(context : Context, log : Logger) {
  def preInit(event : FMLPreInitializationEvent, id : String, name : String, version : String) : Unit = {
    log.info("preInit")

    val metadata : ModMetadata = event.getModMetadata
    metadata.modId = id
    metadata.name = name
    metadata.description = "Fill the world with circuits."
    metadata.url = "https://github.com/Hexirp/akariki"
    metadata.version = version
    metadata.authorList.add("Hexirp")
    metadata.autogenerated = false
  }

  def init(event : FMLInitializationEvent) : Unit = log.info("init")

  def postInit(event : FMLPostInitializationEvent) : Unit = log.info("postInit")
}
